        String pichinchaCantons;
        float length;
        float latitude;

        GeoconverterController geoconvertercontroller = new GeoconverterController();

        pichinchaCantons = cmbPichinchaCantons.getSelectedItem().toString();
        length = Float.parseFloat(txtLength.getText());
        latitude = Float.parseFloat(txtLatitude.getText());

        geoconverter = new Geoconverter (pichinchaCantons,  length, latitude );
        geoconvertercontroller.save(geoconverter);

        JOptionPane.showMessageDialog(rootPane, geoconverter.getPichinchaCantons());
